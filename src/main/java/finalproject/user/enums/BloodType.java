package finalproject.user.enums;

public enum BloodType {
    APLUS {
        public String toString() {
            return "A+";
        }
    },
    AMINUS {
        public String toString() {
            return "A-";
        }
    }, BPLUS {
        public String toString() {
            return "B+";
        }
    }, BMINUS {
        public String toString() {
            return "B-";
        }
    }, ABPLUS {
        public String toString() {
            return "AB+";
        }
    }, ABMINUS {
        public String toString() {
            return "AB-";
        }
    }, ZEROPLUS {
        public String toString() {
            return "0+";
        }
    }, ZEROMINUS {
        public String toString() {
            return "0-";
        }
    },

}
